    public void getEntries() {
        try {
            // Execute the query to get all usernames from the 'users' table
            ResultSet rs = st.executeQuery(
                    "SELECT username,id FROM users order by id asc"
            );

            // Create a new DefaultListModel to store the usernames
            DefaultListModel<String> listModel = new DefaultListModel<>();

            // Iterate over the ResultSet and add each username to the listModel
            while (rs.next()) {
                String userDetail = "Name: " + rs.getString("username") + " Id: " + rs.getInt("id");
                listModel.addElement(userDetail);
            }

            // Set the model to your JList
            jList1.setModel(listModel);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }

