    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            String category = (String) jComboBox2.getSelectedItem();
            String splitMethod = (String) jComboBox1.getSelectedItem();
            int amount = Integer.parseInt(jTextField1.getText());
            if (splitMethod.equals("EQUAL")) {
                java.util.List<String> selectedUsers = jList1.getSelectedValuesList();
                for (String userDetail : selectedUsers) {
                    int userId = Integer.parseInt(userDetail.split(" ")[userDetail.split(" ").length - 1]);
                    PreparedStatement preparedStatement = c.prepareStatement("INSERT INTO spending_participants (split_method, user_id, amount, category) VALUES (?, ?, ?, ?);");
                    preparedStatement.setString(1, splitMethod);
                    preparedStatement.setInt(2, userId);
                    preparedStatement.setInt(3, amount / selectedUsers.size());
                    preparedStatement.setString(4, category);
                    preparedStatement.executeUpdate();
                }
            } else {
                selectedUserList.getCellEditor().stopCellEditing();

                DefaultTableModel model = (DefaultTableModel) selectedUserList.getModel(); // Get the model from the table

                // Iterate over each row
                for (int row = 0; row < model.getRowCount(); row++) {
                    // Get the user name from the first column

                    String userDetail = (String) model.getValueAt(row, 0);

                    // Get the amount entered from the second column (as a String)
                    String percantage = (String) model.getValueAt(row, 1);  // Assuming you're storing amounts as Strings

                    int userId = Integer.parseInt(userDetail.split(" ")[userDetail.split(" ").length - 1]);
                    PreparedStatement preparedStatement = c.prepareStatement("INSERT INTO spending_participants (split_method, user_id, amount, category) VALUES (?, ?, ?, ?);");
                    preparedStatement.setString(1, splitMethod);
                    preparedStatement.setInt(2, userId);
                    if (splitMethod.equals("PERCENTAGE")) {
                        preparedStatement.setInt(3, (int) (amount * (Integer.parseInt(percantage) / 100.0)));
                    } else {
                        preparedStatement.setInt(3, Integer.parseInt(percantage));
                    }
                    preparedStatement.setString(4, category);
                    preparedStatement.executeUpdate();
                }
            }
            JOptionPane.showMessageDialog(null, "Participants Spending Added Successfully!");
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

