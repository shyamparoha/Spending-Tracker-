    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Get the selected users and the split method
        java.util.List<String> selectedUsers = jList1.getSelectedValuesList();
        String splitMethod = (String) jComboBox1.getSelectedItem();

        // Clear all components from jPanel4
        jPanel4.removeAll();

        if (splitMethod.equals("EQUAL")) {
            // Check if the equalSplitLabel is not null and already exists
            if (equalSplitLabel == null) {
                // Create a new label for the "EQUAL" case
                equalSplitLabel = new JLabel();
            }

            // Set the text for the label to display the amount information
            equalSplitLabel.setText("Amount Is Equally Divided Among Selected People!  " + jTextField1.getText());
            equalSplitLabel.setBounds(20, 100, 300, 30); // Set position and size of the label

            // Add the label to jPanel4
            jPanel4.add(equalSplitLabel);
        } else {
            equalSplitLabel = null;

// Define column names for the table
            String[] columnNames = {"User", "Amount"};

            // Prepare the data array
            Object[][] data = new Object[selectedUsers.size()][2];

// Populate the data array with selected users
            for (int i = 0; i < selectedUsers.size(); i++) {
                data[i][0] = selectedUsers.get(i);
                data[i][1] = ""; // Initial empty amount
            }

// Create a DefaultTableModel with the data and column names
            DefaultTableModel model = new DefaultTableModel(data, columnNames);

// Create a JTable with the model
            JTable userTable = new JTable(model);

// Optionally set column widths or other properties
            userTable.getColumnModel().getColumn(0).setPreferredWidth(150); // User column width
            userTable.getColumnModel().getColumn(1).setPreferredWidth(100); // Amount column width

// Add the table to a JScrollPane for scrolling support
            JScrollPane scrollPane = new JScrollPane(userTable);
            scrollPane.setBounds(20, 50, 300, 150); // Set the position and size of the scroll pane

// Add the scrollPane to jPanel4
            jPanel4.add(scrollPane);

            selectedUserList = userTable;
        }

        // Refresh jPanel4 to display the newly added components
        jPanel4.revalidate();
        jPanel4.repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

