    private void getEntries() {
        try {
            // Establishing a connection to the database

            int sno = 0;

            // Executing the query to get data from the database
            ResultSet rs = st.executeQuery("SELECT * FROM category_info");

            // Getting the table model
            DefaultTableModel dtn = (DefaultTableModel) table.getModel();

            // Clearing any existing rows in the table
            dtn.setRowCount(0);
            int rc = dtn.getRowCount();
            while (rc-- != 0) {
                dtn.removeRow(0);
            }

            // Looping through the ResultSet to fetch data and add it to the table
            while (rs.next()) {
                String category = rs.getString("category"); // Replace "category_column_name" with the actual column name
                Object[] o = {++sno, category};
                dtn.addRow(o);
            }

        } catch (Exception ex) {
            System.out.println("Hello error");
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + ex);
        }
    }

