    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // Check if a row is selected
        int ri = table.getSelectedRow();
        if (ri == -1) {
            JOptionPane.showMessageDialog(null, "Please select a category to delete");
            return;
        }

        // Get the category value from the selected row
        String category = (String) table.getValueAt(ri, 1).toString().trim();

        try {
            // Establishing a connection to the database

            // Executing the delete query
            st.executeUpdate("DELETE FROM category_info WHERE category = '" + category + "'");

            // Showing a success message
            JOptionPane.showMessageDialog(null, "Successfully deleted");

            // Refreshing the table data
            getEntries();

            // Closing the Statement and connection
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

