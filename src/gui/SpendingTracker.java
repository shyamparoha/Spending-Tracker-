    private void getEntries() {
        try {
            // Get the table model
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) jTable2.getModel();
            int rc = dtm.getRowCount(); // Number of rows currently in table

            // Removing all rows from the table
            while (rc-- != 0) {
                dtm.removeRow(0);
            }

            // Getting the current date from Java
            java.time.LocalDate cd = java.time.LocalDate.now();
            // Getting 20 days before the current date
            java.time.LocalDate bd = cd.minusDays(20);

            // Executing the query with the correct date range
            ResultSet rs = st.executeQuery(
                    "SELECT * FROM spendings WHERE date <= '" + cd + "' AND date >= '" + bd + "'"
            );

            // Iterating over the result set
            int total = 0;
            while (rs.next()) {
                total += rs.getInt("amount");
                Object[] rowData = {rs.getInt("sid"), rs.getString("category"), rs.getDate("date"), rs.getInt("amount")};
                dtm.addRow(rowData);
            }
            jLabel7.setText(total + "");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }
