    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        try {
            java.util.Date dt = jDateChooser1.getDate();
            String s1 = jTextField1.getText();
            String c = (String) jComboBox1.getSelectedItem();
            String paidBy = (String) jComboBox3.getSelectedItem(); // Assume jComboBoxPaidBy is the "Paid By" combo box

            if (dt != null && !s1.equals("") && !c.equals("") && isSplitMethodClicked && paidBy != null && !paidBy.equals("")) {
                int amount = Integer.parseInt(s1);
                java.sql.Date sqlDate = new java.sql.Date(dt.getTime()); // Convert java.util.Date to java.sql.Date

                st.executeUpdate("INSERT INTO spendings (category, date, amount) VALUES ('" + c + "', '" + sqlDate + "', " + amount + ")");
                JOptionPane.showMessageDialog(null, "Expense added successfully");
                getEntries();

            } else {
                JOptionPane.showMessageDialog(null, "Please fill all fields correctly");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_addbtnActionPerformed

